echo '</div>';
echo '<div class="item-content"><h6 class="item-subtitle mbr-fonts-style mt-1 display-7">' . $video['ph_time'] . ' minutos</h6>';
echo '<p class="mbr-text mbr-fonts-style mt-3 display-7"><a href="https://gean.me/ph-ads.php?v=' . $video['ph_id'] . ' " class="text-primary">' . htmlspecialchars($video['ph_title']) . '</a></p>';
echo '</div></div></div>';
}
