<?php
// URL do feed RSS
$feedUrl = 'https://pt.pornhub.com/video/webmasterss';

// Realiza uma requisição HTTP GET para obter o conteúdo do feed
$feedXml = file_get_contents($feedUrl);

// Verifica se a requisição foi bem-sucedida
if ($feedXml === false) {
    die('Erro ao obter o conteúdo do feed.');
}

// Cria um objeto SimpleXML a partir do XML obtido
$xml = simplexml_load_string($feedXml);

// Verifica se o XML foi carregado corretamente
if ($xml === false) {
    die('Erro ao carregar o XML.');
}

// Array para armazenar os dados dos vídeos
$videos = [];

// Processa os itens (vídeos) no canal
foreach ($xml->channel->item as $item) {
    // Verifica se já coletamos 40 vídeos
    if (count($videos) >= 40) {
        break; // Para o loop se já temos 40 vídeos
    }

    // Extrai e formata os dados do vídeo
    $video = [
        'ph_title' => (string)$item->title,
        'ph_id' => extractVideoId((string)$item->link), // Extrai apenas o ID do vídeo
        'ph_thumb' => str_replace('https://ei.phncdn.com', 'https://i1.wp.com/ei.phncdn.com', (string)$item->thumb_large),
        'ph_keywords' => (string)$item->keywords,
        'ph_date' => formatDate((string)$item->pubDate), // Formata a data de publicação
        'ph_time' => formatDuration((int)$item->duration) // Converte segundos para minutos
    ];

    // Adiciona o vídeo ao array de vídeos
    $videos[] = $video;
}

// Função para extrair o ID do vídeo a partir da URL
function extractVideoId($url) {
    $query = parse_url($url, PHP_URL_QUERY);
    parse_str($query, $params);
    return isset($params['viewkey']) ? $params['viewkey'] : '';
}

// Função para formatar a data de publicação
function formatDate($pubDate) {
    $date = date_create_from_format('D, d M Y H:i:s O', $pubDate);
    if ($date) {
        $date->setTimezone(new DateTimeZone('America/Sao_Paulo'));
        return $date->format('d/m/Y H:i');
    }
    return '';
}

// Função para converter segundos para minutos
function formatDuration($seconds) {
    return round($seconds / 60, 2); // Converte segundos para minutos com 2 casas decimais
}

// Exibir os vídeos na página HTML
